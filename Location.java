
public class Location {
	private int LAT;
	private int LONG;
	public int getLAT() {
		return LAT;
	}
	public void setLAT(int lAT) {
		LAT = lAT;
	}
	public int getLONG() {
		return LONG;
	}
	public void setLONG(int lONG) {
		LONG = lONG;
	}
	public Location(int lAT, int lONG) {
		super();
		LAT = lAT;
		LONG = lONG;
	}
	
	
}
